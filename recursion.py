# Рекурсия может быть не прямой
# Одна функция вызывает другую, которая вызывает вторую и так далее
# Можно задействовать сколько угодно функций

def is_even(x):
    if x == 0:
        return True
    else:
        return is_odd(x - 1)

def is_odd(x):
    return not is_even(x)

