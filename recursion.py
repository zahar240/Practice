s = int(input("Введите число: "))

# Классический пример фунции, реализуемой рекусивно, является функция вычисления факториала
# Функция находит результат умножения всех натуральных чисел ниже заданного числа 
def factorial(x):
    if x == 1:
        return 1
    else:
        return x * factorial(x - 1)

print(factorial(s))

